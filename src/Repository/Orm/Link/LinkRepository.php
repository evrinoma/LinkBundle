namespace Evrinoma\LinkBundle\Repository\Orm\Link;

use Doctrine\Persistence\ManagerRegistry;
use Evrinoma\LinkBundle\Mediator\QueryMediatorInterface;
use Evrinoma\LinkBundle\Repository\Link\LinkRepositoryInterface;
use Evrinoma\LinkBundle\Repository\Link\LinkRepositoryTrait;
use Evrinoma\UtilsBundle\Repository\Orm\RepositoryWrapper;
use Evrinoma\UtilsBundle\Repository\RepositoryWrapperInterface;

class LinkRepository extends RepositoryWrapper implements LinkRepositoryInterface, RepositoryWrapperInterface
{
    use LinkRepositoryTrait;

    /**
     * @param ManagerRegistry        $registry
     * @param string                 $entityClass
     * @param QueryMediatorInterface $mediator
     */
    public function __construct(ManagerRegistry $registry, string $entityClass, QueryMediatorInterface $mediator)
    {
        parent::__construct($registry, $entityClass);
        $this->mediator = $mediator;
    }
}
